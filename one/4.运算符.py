# 运算符
'''
+ 加
- 减
* 乘
/ 除
// 整除
% 取余
** 指数

'''
a = 8
b = 4
c = 9
d = 2
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(c // b)
print(c % b)
print(d ** b)

# 逻辑运算符
print((a < b) and (b > d))    # False
print((a < b) or (b > d))     # True
print(not(a < b))


# 多复制，且付相同值
x = y = z = 11
print(x, y, z)
